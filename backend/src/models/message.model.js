import mongoose, { Schema } from "mongoose";

const messageSchema = new Schema(
    {
        conversation: {
            type: Schema.Types.ObjectId,
            ref: "Conversation",
            required: true,
        },
        sender: {
            type: Schema.Types.ObjectId,
            ref: "User",
            required: true,
        },
        content: {
            type: String,
            required: true,
            trim: true,
        },
        // --- READ RECEIPT LOGIC ---
        isRead: {
            type: Boolean,
            default: false, 
        }
    },
    { timestamps: true }
);

export const Message = mongoose.model("Message", messageSchema);